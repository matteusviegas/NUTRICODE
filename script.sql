-- TABELA USUARIO

CREATE TABLE USUARIO_SP (
    ID_USUARIO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL,
    SENHA VARCHAR2(100) NOT NULL,

    CONSTRAINT PK_USUARIO_SP PRIMARY KEY (ID_USUARIO),
    CONSTRAINT UK_USUARIO_SP_EMAIL UNIQUE (EMAIL)
);

-- TABELA PERFIL

CREATE TABLE PERFIL_SP (
    ID_PERFIL NUMBER GENERATED BY DEFAULT AS IDENTITY,
    IDADE NUMBER,
    OBJETIVO VARCHAR2(100),
    ID_USUARIO NUMBER NOT NULL,

    CONSTRAINT PK_PERFIL_SP PRIMARY KEY (ID_PERFIL),

    CONSTRAINT FK_PERFIL_USUARIO_SP
    FOREIGN KEY (ID_USUARIO)
    REFERENCES USUARIO_SP(ID_USUARIO)
);

-- TABELA RECEITA

CREATE TABLE RECEITA_SP (
    ID_RECEITA NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOME VARCHAR2(100) NOT NULL,
    VALOR_CALORICO NUMBER
);

